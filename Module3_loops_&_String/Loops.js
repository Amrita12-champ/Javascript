// For Loop
console.log("For Loop");
for (let i = 1; i <= 5; i++) {
    console.log(i);
}

// While Loop
console.log("While Loop");
let j = 1;

while (j <= 5) {
    console.log(j);
    j++;
}

// Do While Loop
console.log("Do While Loop");
let k = 1;

do {
    console.log(k);
    k++;
} while (k <= 5);